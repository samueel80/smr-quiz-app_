function processFiles(files) {
    let loadedCount = 0;
    let newCount = 0;
    let duplicateCount = 0;

    Array.from(files).forEach(file => {
        const reader = new FileReader();
        reader.onload = function(event) {
            try {
                const arr = JSON.parse(event.target.result);
                if (Array.isArray(arr)) {
                    arr.forEach(q => {
                        if (q.question && Array.isArray(q.options) && q.answer !== undefined) {
                            let fileSubject = file.name
                                .replace('preguntas_', '')
                                .replace('.json', '')
                                .replace(/_\d+/g, '')
                                .replace(/\d+/g, '')
                                .replace(/_/g, ' ')
                                .trim();

                            let finalSubject = q.subject || fileSubject || "Sin clasificar";
                            finalSubject = finalSubject
                                .replace(/_\d+/g, '')
                                .replace(/\s+\d+/g, '')
                                .replace(/\d+/g, '')
                                .replace(/_/g, ' ')
                                .trim();
                            
                            finalSubject = finalSubject.split(/\b(ud|uds|unidades|unidad|tema|temas)\b/i)[0].trim();
                            
                            if (finalSubject.toUpperCase() === "HCL" || finalSubject.toUpperCase() === "HLC") {
                                finalSubject = "HLC (Horas de Libre Configuraciأ³n)";
                            }
                            if (finalSubject.toLowerCase() === "sistemas operativos en red") {
                                finalSubject = "Sistemas Operativos en Red";
                            }
                            if (finalSubject.toLowerCase() === "seguridad informأ،tica" || finalSubject.toLowerCase() === "seguridad informatica") {
                                finalSubject = "Seguridad Informatica";
                            }

                            const normText = normalizeString(q.question);
                            const exists = db.questions.some(item => normalizeString(item.question) === normText && item.subject === finalSubject);
                            
                            if (!exists) {
                                db.questions.push({
                                    question: q.question,
                                    options: q.options,
                                    answer: q.answer,
                                    subject: finalSubject,
                                    source: q.source || "aأ±adida"
                                });
                                newCount++;
                            } else {
                                duplicateCount++;
                            }
                        }
                    });
                }
                loadedCount++;
                if (loadedCount === files.length) {
                    saveDatabase();
                    showCustomAlert('Importaciأ³n Exitosa', `آ،Importaciأ³n completa!\nPreguntas nuevas: ${newCount}\nDuplicados omitidos: ${duplicateCount}`, 'check_circle');
                }
            } catch (err) {
                showCustomAlert('Error de Formato', `Error en el formato de ${file.name}.\nDetalle: ${err.message}\n\nRevisa que el archivo JSON estأ© bien escrito (comillas dobles, comas, etc).`, 'error');
            }
        };
        reader.readAsText(file);
    });
}

function normalizeString(str) {
    return str.toLowerCase()
              .replace(/[^a-z0-9أ،أ©أ­أ³أ؛أ¼أ±]/g, '')
              .trim();
}

